        <p>Salvador Dalí es conocido por sus impactantes y oníricas imágenes surrealistas. Sus habilidades pictóricas se suelen atribuir a 
            la influencia y admiración por el arte renacentista. También fue un experto dibujante.2​3​ Los recursos plásticos dalinianos también 
            abordaron el cine, la escultura y la fotografía, lo cual le condujo a numerosas colaboraciones con otros artistas audiovisuales. Tuvo 
            la capacidad de acrisolar un estilo genuinamente personal y palpable al primer contacto, que en realidad era muy ecléctico y que «succionó» 
            de innovaciones ajenas. Una de sus pinturas más célebres es La persistencia de la memoria (también conocida como Los relojes blandos), 
            realizada en 1931. 
            Como artista extremadamente imaginativo, manifestó una notable tendencia al narcisismo y la megalomanía, cuyo objeto era atraer la 
            atención pública. Esta conducta irritaba a quienes apreciaban su arte y justificaba a sus críticos, que rechazaban sus conductas
            excéntricas como un reclamo publicitario ocasionalmente más llamativo que su producción artística.4​ Dalí atribuía su «amor por 
            todo lo que es dorado y resulta excesivo, su pasión por el lujo y su amor por la moda oriental» a un autoproclamado «linaje 
            arábigo»,5​ que remontaba sus raíces a los tiempos de la dominación árabe de la península ibérica.</p>
        
        <p>Salvador Dalí nació a las 8:45 horas del 11 de mayo de 1904,7​ en el número 20 de la calle Monturiol, en Figueras, provincia de 
        Gerona,8​ en la comarca catalana del Ampurdán, cerca de la frontera con Francia.8​ El hermano mayor de Dalí, también llamado Salvador 
        (nacido el 12 de octubre de 1901 y bautizado como Salvador Galo Anselmo),1​ había muerto de un «catarro gastroenterítico infeccioso» 
        unos nueve meses antes (el 1 de agosto de 1903). Esto marcó mucho al artista posteriormente, quien llegó a tener una crisis de 
        personalidad, al creer que él era la copia de su hermano muerto.9​ Su padre, Salvador Dalí i Cusí, era abogado de clase media y 
        notario, de carácter estricto suavizado por su mujer Felipa Domènech i Ferrés, quien alentaba los intereses artísticos del joven 
        Salvador.10​11​ Con cinco años, sus padres lo llevaron a la tumba de su hermano y le dijeron que él era su reencarnación, una idea que 
        él llegó a creer.12​13​ De su hermano, Dalí dijo: ... nos parecíamos como dos gotas de agua, pero dábamos reflejos diferentes... Mi hermano
        era probablemente una primera visión de mí mismo, pero según una concepción demasiado absoluta.
        Dalí también tuvo una hermana, Anna Maria Dalí, cuatro años más joven que él.15​ En 1949 ella publicó un libro sobre su hermano, 
        titulado Dalí visto por su hermana.16​ En su infancia, Dalí trabó amistad con futuros jugadores del F.C. Barcelona, como Emilio 
        Sagi Liñán o Josep Samitier. En época de vacaciones, en el floreciente Cadaqués, el trío pasaba mucho tiempo jugando al fútbol.17​
        En 1916 descubrió la pintura contemporánea durante una visita familiar a Cadaqués, donde conoció a la familia de Ramón Pichot, un 
        artista local que viajaba regularmente a París, la capital del arte del momento.10​ Siguiendo los consejos de Pichot, su padre lo 
        envió a clases de pintura con el maestro Juan Núñez. Al año siguiente, su padre organizó una exposición de sus dibujos al carboncillo 
        en la casa familiar. En 1919, con catorce años, Dalí participó en una exposición colectiva de artistas locales en el teatro municipal
        de Figueras y en otra en Barcelona, auspiciada por la Universidad, en la que recibió el premio Rector de la Universidad.
        En 1919, cuando cursaba sexto de bachillerato en el instituto Ramón Muntaner, editaron entre varios amigos la revista mensual Studium. 
        En esta publicación se incluían ilustraciones, textos poéticos y una serie de artículos sobre pintores como Goya, Velázquez o Leonardo 
        da Vinci.18​
        En febrero de 1921 su madre murió a consecuencia de un cáncer de útero. Dalí tenía 16 años. Sobre la muerte de su madre diría más tarde 
        que fue «el golpe más fuerte que he recibido en mi vida. La adoraba. No podía resignarme a la pérdida del ser con quien contaba para 
        hacer invisibles las inevitables manchas de mi alma...».19​ Tras su muerte, el padre de Dalí contrajo matrimonio con la hermana de su 
        esposa fallecida. Dalí nunca aprobó este hecho.</p>
